            <h3 class="text-2xl font-semibold mb-4">What is Image Compression?</h3>
            <p>
              Image compression is the process of reducing a digital image's file size while preserving acceptable image quality. The goal is to eliminate redundant image data to allow for faster loading times, reduced storage space, and efficient transmission across networks.
            </p>
            
            <div class="grid grid-cols-1 md:grid-cols-2 gap-8 my-8">
              <div class="bg-gray-50 dark:bg-gray-700 p-6 rounded-lg">
                <h4 class="text-xl font-semibold mb-3">Lossless Compression</h4>
                <p class="mb-3">
                  Lossless Compression maintains original quality while reducing file size. Lossy Compression achieves smaller sizes by selectively discarding data, potentially affecting quality
                </p>
                <p class="text-sm text-gray-600 dark:text-gray-400">
                  <strong>Best for:</strong> Graphics, text, illustrations, or images where every pixel detail matters.
                </p>
                <p class="text-sm text-gray-600 dark:text-gray-400">
                  <strong>Formats:</strong> PNG, GIF, some specialized JPEG modes, TIFF with specific compression options.
                </p>
              </div>
              
              <div class="bg-gray-50 dark:bg-gray-700 p-6 rounded-lg">
                <h4 class="text-xl font-semibold mb-3">Lossy Compression</h4>
                <p class="mb-3">
                  Lossy compression achieves greater file size reduction by permanently removing certain image data. It identifies and discards information that the human eye is less likely to notice.
                </p>
                <p class="text-sm text-gray-600 dark:text-gray-400">
                  <strong>Best for:</strong> Photographs, complex images with many colors, web graphics.
                </p>
                <p class="text-sm text-gray-600 dark:text-gray-400">
                  <strong>Formats:</strong> JPEG, WebP, AVIF, HEIC.
                </p>
              </div>
            </div>
            
            <h3 class="text-2xl font-semibold mb-4">How Image Compression Works</h3>
            <p>
              Behind the scenes, image compression algorithms use various mathematical techniques to reduce file size:
            </p>
            
            <ul class="list-disc pl-6 space-y-2 mt-4">
              <li>
                <strong>Chroma Subsampling:</strong> Reduces color information (which human eyes are less sensitive to) while preserving luminance/brightness details.
              </li>
              <li>
                <strong>Discrete Cosine Transform (DCT):</strong> Converts spatial image data into frequency components, allowing less important frequencies to be reduced or eliminated (used in JPEG).
              </li>
              <li>
                <strong>Run-Length Encoding:</strong> Compresses data by storing runs of identical pixels as single values with count numbers.
              </li>
              <li>
                <strong>Dictionary Coding:</strong> Replaces recurring patterns with references to a dictionary of those patterns (used in PNG compression).
              </li>
              <li>
                <strong>Predictive Coding:</strong> Encodes the difference between the predicted value of a pixel and its actual value.
              </li>
            </ul>
            
            <h3 class="text-2xl font-semibold mt-8 mb-4">Finding the Right Balance</h3>
            <p>
              The key to effective image compression is finding the optimal balance between file size and image quality. Higher compression rates can significantly reduce file size but may introduce visible artifacts or loss of detail. Our image optimizer provides interactive controls that let you adjust compression levels while seeing the results in real-time, enabling you to find the perfect balance for your specific needs.
            </p>
            
